module.exports = `available commands:\n
add "$title" "$artist": adds an album to the collection with the given title and artist\n
play "$title": plays an album\n
show all: displays all of the albums in the collection\n
show unplayed: displays all of the albums that are unplayed\n
show all by "$artist": shows all of the albums in the collection by the given artist\n
show unplayed by "$artist": shows the unplayed albums in the collection by the given artist\n
quit: quits the program`
